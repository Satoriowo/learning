# if statement = block of code that will execute if it's condition is true

age = int(input('How old are you?: '))

if age == 100:
    print("You are too old, like a century!")
elif age >= 18:
    print('You are an adult, sad :c')
elif age < 0:
    print("You haven't born yet dudee!")
else : 
    print('You are a little kid! :3')
